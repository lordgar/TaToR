package com.olsonusa.TaToR.core;

public class PointsMeasure
{
    private int pointMaximum;
    private int pointsCurrent;
    private int temporaryMax;

    public int getPointMaximum()
    {
        return pointMaximum;
    }

    public void setPointMaximum(int pointMaximum)
    {
        this.pointMaximum = pointMaximum;
    }

    public int getPointsCurrent()
    {
        return pointsCurrent;
    }

    public void setPointsCurrent(int pointsCurrent)
    {
        this.pointsCurrent = pointsCurrent;
    }

    public int getTemporaryMax()
    {
        return temporaryMax;
    }

    public void setTemporaryMax(int temporaryMax)
    {
        this.temporaryMax = temporaryMax;
    }
}
